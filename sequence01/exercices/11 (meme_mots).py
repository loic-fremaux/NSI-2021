def meme_mots(first: str, second: str):
    """
    check if two words are the sames
    :param first: first word to compare
    :param second: second word to compare
    :return: True if both words are the same or else False
    """
    return first == second


print(meme_mots("test", "tset"))
print(meme_mots("test", "test"))
